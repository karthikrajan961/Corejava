public class Calculator{

 public int sum(int i, int j){
   int sum = i + j;
   return(sum);
 }

 public int diff(int i, int j){
  int diff = i - j;
  return(diff);
 }

 public static int div(int i, int j){
  int div = i / j;
  return(div);
 }

 public static int prod(int i, int j){
 int prod = i * j;
 return(prod);
 }
}